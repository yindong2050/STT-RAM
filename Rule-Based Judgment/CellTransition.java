package com.java.model;

public class CellTransition {
	
	private long ZT;
	
	private long ST;
	
	private long HT;
	
	private long TT;
	
	public long getZT() {
		return ZT;
	}
	public void setZT(long zT) {
		ZT = zT;
	}
	public long getST() {
		return ST;
	}
	public void setST(long sT) {
		ST = sT;
	}
	public long getHT() {
		return HT;
	}
	public void setHT(long hT) {
		HT = hT;
	}
	public long getTT() {
		return TT;
	}
	public void setTT(long tT) {
		TT = tT;
	}
}
